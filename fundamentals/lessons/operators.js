// Operadores relacionales o de comparaciÃ³n (devuelven true o false)

// > Mayor que
console.log(10 > 5); // true

// < Menor que
console.log(5 < 10); // true

// >= Mayor o igual que
console.log(10 >= 10); // true

// <= Menor o igual que
console.log(5 <= 10); // true

// == Igualdad (compara valores, no tipos)
console.log(1 == "1"); // true

// === Igualdad estricta (compara valores y tipos)
console.log(1 === "1"); // false
console.log(1 === 1); // true

// != Diferente (compara valores, no tipos)
console.log(1 != "1"); // false

// !== Diferente estricto (compara valores y tipos)
console.log(1 !== "1"); // true

// Ejemplo prÃ¡ctico con variables
var x = 1;
console.log(x == "1"); // true
console.log(x === "1"); // false
